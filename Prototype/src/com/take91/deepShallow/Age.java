package com.take91.deepShallow;

public class Age {
	public Age(int year, int value) {
		super();
		this.year = year;
		this.value = value;
	}

	int year;

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	int value;
}
